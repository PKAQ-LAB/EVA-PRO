import React, { Component } from 'react';

import { Dispatch } from 'redux';
import { FormattedMessage } from 'umi';
import { Menu } from 'antd';
import { connect } from 'umi';
import BaseView from './components/base';
import BindingView from './components/binding';
import { CurrentUser } from './data.d';
import NotificationView from './components/notification';
import SecurityView from './components/security';
import styles from './style.less';
import RePwd from './resetpwd';

const { Item } = Menu;

interface SettingProps {
  dispatch: Dispatch<any>;
  currentUser: CurrentUser;
}

type SettingStateKeys = 'base' | 'security' | 'binding' | 'notification';
interface SettingState {
  mode: 'inline' | 'horizontal';
  menuMap: {
    [key: string]: React.ReactNode;
  };
  selectKey: SettingStateKeys;
}
@connect((
  { userSetting, global }: { userSetting: { currentUser: CurrentUser }, global : { repwd: boolean } }
  ) => ({
  repwd: global.repwd,
  currentUser: userSetting.currentUser,
}))
class Setting extends Component<SettingProps, SettingState> {
  main: HTMLDivElement | undefined = undefined;

  constructor(props: SettingProps) {
    super(props);
    const menuMap = {
      base: <FormattedMessage id="user-setting.menuMap.basic" defaultMessage="Basic Settings" />,
      security: (
        <FormattedMessage id="user-setting.menuMap.security" defaultMessage="Security Settings" />
      ),
      binding: (
        <FormattedMessage id="user-setting.menuMap.binding" defaultMessage="Account Binding" />
      ),
      notification: (
        <FormattedMessage
          id="user-setting.menuMap.notification"
          defaultMessage="New Message Notification"
        />
      ),
    };
    this.state = {
      mode: 'inline',
      menuMap,
      selectKey: 'base',
    };
  }

  componentDidMount() {
    const { dispatch } = this.props;
    dispatch({
      type: 'userSetting/fetchCurrent',
    });
    window.addEventListener('resize', this.resize);
    this.resize();
  }

  componentWillUnmount() {
    window.removeEventListener('resize', this.resize);
  }

  getMenu = () => {
    const { menuMap } = this.state;
    return Object.keys(menuMap).map(item => <Item key={item}>{menuMap[item]}</Item>);
  };

  getRightTitle = () => {
    const { selectKey, menuMap } = this.state;
    return menuMap[selectKey];
  };

  selectKey = (key: SettingStateKeys) => {
    this.setState({
      selectKey: key,
    });
  };

  resize = () => {
    if (!this.main) {
      return;
    }
    requestAnimationFrame(() => {
      if (!this.main) {
        return;
      }
      let mode: 'inline' | 'horizontal' = 'inline';
      const { offsetWidth } = this.main;
      if (this.main.offsetWidth < 641 && offsetWidth > 400) {
        mode = 'horizontal';
      }
      if (window.innerWidth < 768 && offsetWidth > 400) {
        mode = 'horizontal';
      }
      this.setState({
        mode,
      });
    });
  };

  renderChildren = () => {
    const { selectKey } = this.state;
    switch (selectKey) {
      case 'base':
        return <BaseView />;
      case 'security':
        return <SecurityView dispatch={this.props.dispatch} />;
      case 'binding':
        return <BindingView />;
      case 'notification':
        return <NotificationView />;
      default:
        break;
    }

    return null;
  };

  render() {
    const { currentUser, repwd } = this.props;

    if (!currentUser.userid) {
      return '';
    }
    const { mode, selectKey } = this.state;
    return (
      <>
      <div
        className={styles.main}
        ref={ref => {
          if (ref) {
            this.main = ref;
          }
        }}
      >
        <div className={styles.leftMenu}>
          <Menu
            mode={mode}
            selectedKeys={[selectKey]}
            onClick={({ key }) => this.selectKey(key as SettingStateKeys)}
          >
            {this.getMenu()}
          </Menu>
        </div>
        <div className={styles.right}>
          <div className={styles.title}>{this.getRightTitle()}</div>
          {this.renderChildren()}
        </div>
      </div>
      {repwd && <RePwd />}
      </>
    );
  }
}

export default Setting;
